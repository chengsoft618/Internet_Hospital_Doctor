    /**
     * Convert string to List.
     *
     * @param str    The string.
     * @param filter 筛选条件
     * @return the SBC string
     */
    public static List<String> str2ArrayList(String str, char filter) {
        List<String> strings = new ArrayList<>();
        if (str == null || str.length() == 0) {
            return strings;
        }
        char[] chars = str.toCharArray();
        int start = 0;
        int end = 0;
        int length = chars.length;
        for (int i = 0, len = length; i < len; i++) {
            if (chars[i] == filter) {
                end = i;
                strings.add(str.substring(start, end));
                start = end + 1;
            }
        }
        if (chars[length - 1] != filter) {
            strings.add(str.substring(start, length));
        }
        return strings;
    }

